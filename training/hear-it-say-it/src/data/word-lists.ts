export const WORD_LISTS: WordLists = {
  3: THREE_LETTER_WORDS,
  4: FOUR_LETTER_WORDS,
  5: FIVE_LETTER_WORDS,
};

/**
 * Get all words of a specific length
 */
export function getWordsByLength(length: WordLength): string[] {
  return WORD_LISTS[length];
}

/**
 * Get characters contained in a word
 */
export function getWordCharacters(word: string): string[] {
  return word.toUpperCase().split('');
}

/**
 * Filter words to only those containing characters from the active set
 */
export function filterWordsByActiveChars(
  words: string[],
  activeChars: string[]
): string[] {
  const activeSet = new Set(activeChars.map(c => c.toUpperCase()));
  return words.filter(word =>
    word.split('').every(char => activeSet.has(char))
  );
}
